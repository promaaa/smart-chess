# --- PRÉ-CALCUL OPTIMISÉ (Table unifiée) ---
# Structure : tables[piece_type][color][square]
# Cela permet un accès direct table[p][c][s] sans conditionnelle complexe

tables_mg = [[0] * 64 for _ in range(7)]  # 1 à 6 (Pawn à King)
tables_eg = [[0] * 64 for _ in range(7)]

# Mapping temporaire pour construction
raw_mg = {
    chess.PAWN: MG_PAWN,
    chess.KNIGHT: MG_KNIGHT,
    chess.BISHOP: MG_BISHOP,
    chess.ROOK: MG_ROOK,
    chess.QUEEN: MG_QUEEN,
    chess.KING: MG_KING,
}
raw_eg = {
    chess.PAWN: EG_PAWN,
    chess.KNIGHT: EG_KNIGHT,
    chess.BISHOP: EG_BISHOP,
    chess.ROOK: EG_ROOK,
    chess.QUEEN: EG_QUEEN,
    chess.KING: EG_KING,
}

# On construit les tables finales
# Index 0: Blancs, Index 1: Noirs
final_tables_mg = {pt: [None, None] for pt in range(1, 7)}
final_tables_eg = {pt: [None, None] for pt in range(1, 7)}

for pt in range(1, 7):
    # Blancs (Index 0)
    white_mg = [val + MG_VALS[pt - 1] for val in raw_mg[pt]]
    white_eg = [val + EG_VALS[pt - 1] for val in raw_eg[pt]]

    # Noirs (Index 1) - On applique le miroir (square ^ 56) ET la négation ici
    # Comme ça, au runtime, on additionne juste la valeur, quel que soit le camp.
    black_mg = [-(raw_mg[pt][i ^ 56] + MG_VALS[pt - 1]) for i in range(64)]
    black_eg = [-(raw_eg[pt][i ^ 56] + EG_VALS[pt - 1]) for i in range(64)]

    final_tables_mg[pt][0] = white_mg
    final_tables_mg[pt][1] = black_mg
    final_tables_eg[pt][0] = white_eg
    final_tables_eg[pt][1] = black_eg


class Engine:
    def __init__(self):
        pass

    def evaluate(self, board):
        """
        Évaluateur Tapered (Progressif) hautement optimisé.
        Interpole entre MG et EG selon le matériel restant.
        """
        # Détections de fin de partie immédiates
        if board.is_checkmate():
            return -99999 if board.turn == chess.WHITE else 99999
        if board.is_insufficient_material() or board.is_stalemate():
            return 0

        mg_score = 0
        eg_score = 0
        phase = TOTAL_PHASE

        # Boucle optimisée sur le piece_map (évite les 64 cases)
        # pm est un dictionnaire {square: Piece}
        pm = board.piece_map()

        for square, piece in pm.items():
            pt = piece.piece_type
            # Astuce Python: int(True)=1, int(False)=0.
            # chess.WHITE est True, chess.BLACK est False.
            # Mais nos tables sont indexées [0] pour Blancs, [1] pour Noirs.
            # Donc color_idx = 0 si WHITE, 1 si BLACK.
            # chess.WHITE (True) -> doit donner 0
            # chess.BLACK (False) -> doit donner 1
            c_idx = 0 if piece.color else 1

            # Mise à jour des scores
            mg_score += final_tables_mg[pt][c_idx][square]
            eg_score += final_tables_eg[pt][c_idx][square]

            # Mise à jour de la phase (soustraction du matériel présent)
            # On réduit la phase si on trouve des pièces majeures/mineures
            if pt != chess.PAWN and pt != chess.KING:
                phase -= PHASE_WEIGHTS[pt]

        # Clamp de la phase entre 0 et 24 (sécurité)
        phase = (phase * 256 + (TOTAL_PHASE / 2)) // TOTAL_PHASE
        # Note: la logique habituelle est:
        # GamePhase = TotalPhaseStart - CurrentPhaseMaterial
        # Ici pour simplifier l'algo de phase, on recalcule la "Phase restante"
        # Phase "Haut" (24) = Début de jeu. Phase "Bas" (0) = Finale pure.

        # Recalcul propre de la phase pour l'interpolation standard
        # On réinitialise la phase pour le calcul correct ci-dessous
        # (La méthode ci-dessus était une approximation, voici la version exacte)

        current_phase = 0
        for piece in pm.values():
            if piece.piece_type in PHASE_WEIGHTS:
                current_phase += PHASE_WEIGHTS[piece.piece_type]

        if current_phase > 24:
            current_phase = 24

        # Interpolation Tapered
        # Formule : (MG * Phase + EG * (24 - Phase)) / 24
        final_score = (mg_score * current_phase + eg_score * (24 - current_phase)) // 24

        # Perspective du joueur (NegaMax nécessite un score relatif)
        return final_score if board.turn == chess.WHITE else -final_score
